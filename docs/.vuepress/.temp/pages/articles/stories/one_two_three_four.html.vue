<h2 id="独坐莫凭栏" tabindex="-1"><a class="header-anchor" href="#独坐莫凭栏" aria-hidden="true">#</a> 独坐莫凭栏</h2>
<p>这是这句俗语的最后一句，说的是一个人不要去河边或靠着高处的栏杆。为什么要这么说呢？话说古代有个武功特别高强的人，遭到自己师兄弟们的嫉妒，于是就在他每天休息的栏杆上做了手脚，他们把其中的一处栏杆弄断，在粘好，等到这个人，在栏杆处休息时，栏杆断了最后掉下去摔死了……</p>
<p>而且，当我一个人独坐的时候，很容易想到一些不好的悲伤的事情，很容易想不开，所以告诫大家独坐莫凭栏。</p>
