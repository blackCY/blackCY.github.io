<h2 id="四人不回头" tabindex="-1"><a class="header-anchor" href="#四人不回头" aria-hidden="true">#</a> 四人不回头</h2>
<p>这句话有个传说，话说当年诸葛亮去世后，曾留下“四人抬棺不回头，绳断之后需磕头”的遗命。</p>
<p>所以，当诸葛亮去世后，蜀汉皇帝刘婵就按照遗命，在军中挑选了 4 名士兵，让他们四人抬着诸葛亮的棺木，从定军上一直往南走，并命令中途不能回头，直到绳子自己磨断，然后在绳断处磕头下拜，再将棺木就地下葬。</p>
<p>可是这四名士兵，走了很久实在是太累了，于是就决定用石头砸断绳子，就地掩埋，然后返回。可是，最后刘禅派人调查，发现绳子有被石头砸断的痕迹，于是拆穿了四人的谎言，并将四名士兵处死。这才有了四人不回头的说法。</p>
